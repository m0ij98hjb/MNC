export function getStats() {
  const totalPhotos = PROJECTS.reduce((s, p) => s + p.gallery.length, 0);
  const cities = [...new Set(PROJECTS.map((p) => p.location.ar))];
  return {
    projects: PROJECTS.length,
    photos: totalPhotos,
    categories: Object.keys(CATEGORIES).length - 1, // exclude "all"
    cities: cities.length,
  };
}

export function getProjectsByCategory(categoryId) {
  if (categoryId === "all") return PROJECTS;
  return PROJECTS.filter((p) => p.category === categoryId);
}
